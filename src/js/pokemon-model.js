class Pokemon {
    constructor(number, name, types, photo, abilities, moves, baseStats, height, weight) {
        this.number = number;            
        this.name = name;                 
        this.types = types;               
        this.photo = photo;            
        this.abilities = abilities;      
        this.moves = moves;               
        this.baseStats = baseStats;       
        this.height = height;             
        this.weight = weight;             
    }
}